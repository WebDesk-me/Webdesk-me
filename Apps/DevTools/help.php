<h5>About</h5>
<p>
Developer's Tools allows you to develop custom apps that extend the functionality of your WebDesk. 
</p>
<h5>Create a new app</h5>
<p>
	Press <span class="webdesk_bg-light"><i class="fa fa-plus"></i> New Project</span> to create a new project. Projects should contain the following files:
	<ul>
		<li><i>app.json</i> &mdash; The library file containing information about the project and any dependency requirements</li>
		<li><i>start.php</i> &mdash; The initial project file loaded into the browser</li>
		<li><i>header.php</i> &mdash; Header files, such as links to scripts and stylesheets, loaded into the &lt;head&gt;</li>
	</ul>
</p>
